    private void BtnPresupuestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPresupuestoActionPerformed
        // TODO add your handling code here:
        String Selec = CBProductos.getSelectedItem().toString();
        if (Selec == "Seleccione alguno de nuestros de productos...") {
            JOptionPane.showMessageDialog(this, "No ha seleccionado algún producto. Por favor, intente de nuevo.", "CONFIGURACION", JOptionPane.WARNING_MESSAGE);
        } else {
            FrmDinero PorPresupuesto = new FrmDinero();
            PorPresupuesto.setVisible(true);
            PorPresupuesto.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            this.setVisible(false);
        }

    }//GEN-LAST:event_BtnPresupuestoActionPerformed

