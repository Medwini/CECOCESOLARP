    private void CBProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CBProductosActionPerformed
        // TODO add your handling code here:
        String Selec = CBProductos.getSelectedItem().toString();
        int Cod_Producto = Conec.Consult_DispoCod(Selec);
        double PrecioU = Conec.Consult_DispoPrecioU(Cod_Producto);
        String Descrip = Conec.Consult_DispoDescrip(Cod_Producto);

        TxtNombre.setText(Selec);
        TxtPrecioU.setText("" + PrecioU + "");
        TxtDescripcion.setText(Descrip);

        //VARIABLES PUBLICAS
        Nombre_Prod = Selec;
        PrecioU_Prod = PrecioU;
    }//GEN-LAST:event_CBProductosActionPerformed

